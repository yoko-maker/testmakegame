def show_fragment(stage_no: int):
    """ステージ番号に対応する AKIRA の人格断片を1枚表示する。"""
    frag = FRAGMENTS.get(stage_no)
    if not frag:
        return
    icon = {"sns": "🗨", "voice": "🔊", "memo": "📝", "last": "✉"}.get(frag["kind"], "▌")
    st.markdown(f"#### {icon} 失踪者の断片")
    st.caption(frag["head"])
    style.boxed(frag["body"])
    state.add_clue(frag["clue"])


def collected_fragment_count() -> int:
    """これまでに収集した AKIRA 断片の数 (clue 文字列から数える)。"""
    return sum(
        1 for c in state.game()["clues"] if c.startswith("AKIRAの断片")
    )


def show_last_message():
    """全断片を集めたプレイヤーへ、AKIRA 最後のメッセージを提示する。"""
    style.boxed(LAST_MESSAGE)


